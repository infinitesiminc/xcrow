import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS")
    return new Response(null, { headers: corsHeaders });

  try {
    const { messages, checkpoint } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are an expert AI oversight coach helping a student understand a specific checkpoint from a Level 2 "Guided Audit" simulation. Your job is to deepen their understanding of this ONE concept — not to coach careers or search for roles.

## Checkpoint Context
Area: ${checkpoint.area}
Question posed: ${checkpoint.question}
Correct verdict: ${checkpoint.correctVerdict}
Explanation: ${checkpoint.explanation}
Real-world example: ${checkpoint.realWorldExample}
Coach tip: ${checkpoint.coachTip}

## Your approach:
- Answer questions DIRECTLY about this checkpoint topic
- Give concrete, practical examples from real marketing/analytics scenarios
- If they ask "how would I detect this", give specific steps, metrics, or tools
- If they ask "what guardrail prevents this", suggest concrete process or system changes
- Keep answers focused (3-6 sentences), use markdown for structure
- Use 1-2 emoji max, be professional but approachable
- Do NOT search for roles, suggest simulations, or redirect to other features
- Do NOT use narrowing questions — they asked something specific, answer it`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages.slice(-6),
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const status = response.status;
      if (status === 429) {
        return new Response(JSON.stringify({ error: "Rate limited, please try again shortly." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (status === 402) {
        return new Response(JSON.stringify({ error: "Credits exhausted. Add funds in Settings." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", status, t);
      return new Response(JSON.stringify({ error: "AI error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("checkpoint-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
